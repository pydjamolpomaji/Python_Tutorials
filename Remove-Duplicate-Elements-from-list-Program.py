# Remove duplicate elements from list using for loop in Python
number = [1, 5, 7, 2, 6, 9, 8, 5, 6, 2, 1, 0, 3, 5]
unique = []
for num in number:
    if num not in unique:
        unique.append(num)
        unique.sort()
print(unique)
